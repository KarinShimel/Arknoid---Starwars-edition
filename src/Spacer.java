/**
 * Class Spacer.
 *
 * @author KArin Shimel.
 */
public class Spacer {
    private String symbol;
    private int width;

    /**
     * Sets width.
     *
     * @param width1 the width
     */
    public void setWidth(int width1) {
        this.width = width1;
    }

    /**
     * Sets symbol.
     *
     * @param symbol1 the symbol
     */
    public void setSymbol(String symbol1) {
        this.symbol = symbol1;
    }

    /**
     * Gets width.
     *
     * @return the width
     */
    public int getWidth() {
        return width;
    }

    /**
     * Gets symbol.
     *
     * @return the symbol
     */
    public String getSymbol() {
        return symbol;
    }
}
